    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable2.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please Select To Update", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            String starttime = jTextField1.getText();
            String endtime = jTextField2.getText();
            String id = String.valueOf(jTable2.getValueAt(row, 0));

            if (starttime.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Start Time", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (endtime.isEmpty()) {

                JOptionPane.showMessageDialog(this, "Please Enter End Time", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                try {

                    ResultSet resultSet = mySQL.executeSearch("SELECT * FROM  `time_Slot` WHERE `start_time`='" + starttime + "' AND `end_time`='" + endtime + "'");

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "Alredy Registered", "Warning", JOptionPane.WARNING_MESSAGE);

                    } else {
                        mySQL.executeIUD("UPDATE `time_slot` SET "
                                + "`start_time`='" + starttime + "',`end_time`='" + endtime + "' WHERE `id`='" + id + "'");

                    }

                    loadTimeSlotTable();
                    reset();
                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

