    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String starttime = jTextField1.getText();
        String endtime = jTextField2.getText();

        if (starttime.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Start Time", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (endtime.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Enter End Time", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {

                ResultSet resultSet = mySQL.executeSearch("SELECT * FROM  `time_Slot` WHERE `start_time`='" + starttime + "' AND `end_time`='" + endtime + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Alredy Registered", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    mySQL.executeIUD("INSERT INTO `time_slot`(`start_time`,`end_time`)"
                            + "VALUES('" + starttime + "','" + endtime + "') ");
                }

                loadTimeSlotTable();
                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

