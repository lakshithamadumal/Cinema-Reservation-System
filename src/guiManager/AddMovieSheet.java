    private void loadhnmbr() {
        try {
            java.sql.ResultSet result = mySQL.executeSearch("SELECT * FROM `hall`");

            Vector<String> hsheet = new Vector<>();
            hsheet.add("Select");

            while (result.next()) {
                hsheet.add(result.getString("hall_number"));
                sheetMap.put(result.getString("id"), result.getString("hall_number"));

            }
            DefaultComboBoxModel hnmbr = new DefaultComboBoxModel(hsheet);
            jComboBox1.setModel(hnmbr);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loardCompany() {

        try {
            java.sql.ResultSet result = mySQL.executeSearch("SELECT * FROM `hall` INNER JOIN `sheet` ON `hall`.`id`=`sheet`.`hall_id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);

            while (result.next()) {
                Vector<String> v = new Vector<>();
                v.add(result.getString("id"));
                v.add(result.getString("hall_number"));
                v.add(result.getString("sheet.number"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

