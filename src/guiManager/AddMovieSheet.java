    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String sheetnmbr = jTextField1.getText();
            String hallnmbr = String.valueOf(jComboBox1.getSelectedItem());

            java.sql.ResultSet result = mySQL.executeSearch("SELECT * FROM `hall`  "
                    + "INNER JOIN `sheet` ON `hall`.`id`=`sheet`.`hall_id` "
                    + "WHERE `hall`.`hall_number`='" + hallnmbr + "' AND `sheet`.`number`='" + sheetnmbr + "'");

            if (result.next()) {

                JOptionPane.showMessageDialog(this, "Already Added", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                if (hallnmbr.equals("Select")) {
                    JOptionPane.showMessageDialog(this, "Please select a Hall ", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (sheetnmbr.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Enter Sheet No ", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    mySQL.executeIUD("INSERT INTO `sheet`(`number`,`hall_id`) VALUES('" + sheetnmbr + "','" + hallnmbr + "')");
                    loardCompany();
                    JOptionPane.showMessageDialog(this, "Successfully Added sheet number", "Success", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

