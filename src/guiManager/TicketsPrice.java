    private void loardCustomerType() {

        try {

            java.sql.ResultSet resultSet = mySQL.executeSearch("SELECT* FROM `movie_customer_type`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("customer_type_name"));
                custermerTypeMap.put(resultSet.getString("customer_type_name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel defultcomboboxmodel = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(defultcomboboxmodel);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loardDimension() {

        try {

            java.sql.ResultSet resultSet = mySQL.executeSearch("SELECT* FROM `movie_dimension`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("type"));
                dimensionTypeMap.put(resultSet.getString("type"), resultSet.getString("id"));
            }

            DefaultComboBoxModel defualtcomboboxmodel = new DefaultComboBoxModel(vector);
            jComboBox3.setModel(defualtcomboboxmodel);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loardTable() {
        try {

            java.sql.ResultSet resultSet = mySQL.executeSearch("SELECT* FROM `ticket` "
                    + "INNER JOIN `movie_customer_type` ON `ticket`.`movie_customer_type_id` = `movie_customer_type`.`id`"
                    + "INNER JOIN `movie_dimension` ON `ticket`.`movie_dimension_id` = `movie_dimension`.`id` ORDER BY `ticket`.`id` ASC ");

            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0);

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("movie_customer_type.customer_type_name"));
                vector.add(resultSet.getString("movie_dimension.type"));
                vector.add(resultSet.getString("ticket_price"));

                tableModel.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

