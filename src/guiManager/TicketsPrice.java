    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jComboBox2.grabFocus();

        try {

            String custermerType = String.valueOf(jComboBox2.getSelectedItem());
            String dimensionType = String.valueOf(jComboBox3.getSelectedItem());
            String ticketPrice = jFormattedTextField2.getText();

            if (custermerType.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Select a Custermer Type !", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (dimensionType.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Select a Dimension Type !", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (ticketPrice.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter the Ticket Price !", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                java.sql.ResultSet resultSet = mySQL.executeSearch(
                        "SELECT * FROM `ticket` WHERE `movie_customer_type_id` = '" + custermerTypeMap.get(custermerType) + "' "
                        + "AND `movie_dimension_id` = '" + dimensionTypeMap.get(dimensionType) + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this,
                            "A ticket for this Customer Type and Dimension already exists!",
                            "Warning",
                            JOptionPane.WARNING_MESSAGE);
                } else {
                    // Insert new ticket
                    mySQL.executeIUD("INSERT INTO `ticket` (`ticket_price`, `movie_customer_type_id`, `movie_dimension_id`) "
                            + "VALUES ('" + ticketPrice + "', '" + custermerTypeMap.get(custermerType) + "', '" + dimensionTypeMap.get(dimensionType) + "')");

                    loardCustomerType();
                    loardDimension();
                    loardTable();
                    reset();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

